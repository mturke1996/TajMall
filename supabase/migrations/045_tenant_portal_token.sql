-- بدلاً من إنشاء حسابات Supabase Auth حقيقية للمستأجرين (وهو ما كان
-- سيتطلب إعادة كتابة كل سياسات RLS في التطبيق لتفصل بين "موظف" و
-- "مستأجر" — تغيير كبير وخطر أمني حقيقي إن نُفّذ بعجلة)، تُستخدم رموز
-- وصول عشوائية طويلة (portal_token) لكل مستأجر. الصفحة العامة
-- /portal/[token] تُنفَّذ بالكامل على الخادم بمفتاح service_role
-- (لا تستخدم جلسة المستخدم مطلقاً)، فلا حاجة لأي تعديل على سياسات RLS
-- الحالية، ولا خطر تسريب بيانات موظفين لأي زائر.
-- ============================================================

ALTER TABLE public.contacts
  ADD COLUMN IF NOT EXISTS portal_token text UNIQUE;

CREATE INDEX IF NOT EXISTS idx_contacts_portal_token ON public.contacts (portal_token);
